                $this->Flash->success(__('The user has been saved.'), ['key' => $flash_key]);
            } else {
                $this->Flash->error(__('The user could not be saved. Please, try again.'), ['key' => $flash_key]);
            }
        }
        $this->set(compact('user'));
        $this->set('_serialize', ['user']);
        
